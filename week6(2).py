# Probability examples

# Probability of rolling a 4
dice_prob = 1/6
print("Probability of rolling 4:", dice_prob)

# Probability of heads
coin_prob = 1/2
print("Probability of heads:", coin_prob)

# Probability of heads twice
heads_twice = (1/2) * (1/2)
print("Probability of heads twice:", heads_twice)

# Probability of even number
even_prob = 3/6
print("Probability of even number:", even_prob)
